for (const locale of ['vi', 'en', 'ja', 'ko', 'zh']) {
  const filePath = path.join(messagesDir, `${locale}.json`)
  const raw = fs.readFileSync(filePath, 'utf8')
  const json = JSON.parse(raw)

  if (json.games && json.games.chinese_chess) {
    console.log(`${locale}.json already has chinese_chess — skipping`)
    continue
  }

  if (!json.games) json.games = {}
  json.games.chinese_chess = translations[locale]

  fs.writeFileSync(filePath, JSON.stringify(json, null, 2), 'utf8')
  console.log(`${locale}.json — chinese_chess section added (${Object.keys(translations[locale]).length} keys)`)
}

